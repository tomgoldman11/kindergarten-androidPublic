package il.co.grauman.kindergarten.bl.reports;

public class ReportsRequest {
    int month , year;

    public ReportsRequest(int month, int year) {
        this.month = month;
        this.year = year;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }
}
